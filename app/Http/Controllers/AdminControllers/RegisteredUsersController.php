     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        //
        $this->validate($request, [
            'type' => 'required'
        ]);

        switch($request->query('type')){
            case 'retailers':
                $data['users'] = User::retailers()->get();
                break;

            case 'distributors':
                $data['users'] = User::distributors()->get();
                break;
        }

        $data['type'] = ucwords($request->query('type'));

        return view('admin.main.registered_users', $data);

    }

